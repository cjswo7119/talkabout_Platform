package com.inhatc.talkabout.common.vo;

public class ChatVo {
    private String reltNo;
    private String reltKwrd;
    private String cntKwrd;
    private String daily;
    private String kwRank;
    private String kwDate;

    public String getReltNo() {
        return reltNo;
    }

    public void setReltNo(String reltNo) {
        this.reltNo = reltNo;
    }

    public String getReltKwrd() {
        return reltKwrd;
    }

    public void setReltKwrd(String reltKwrd) {
        this.reltKwrd = reltKwrd;
    }

    public String getCntKwrd() {
        return cntKwrd;
    }

    public void setCntKwrd(String cntKwrd) {
        this.cntKwrd = cntKwrd;
    }

    public String getDaily() {
        return daily;
    }

    public void setDaily(String daily) {
        this.daily = daily;
    }

    public String getKwRank() {
        return kwRank;
    }

    public void setKwRank(String kwRank) {
        this.kwRank = kwRank;
    }

    public String getKwDate() {
        return kwDate;
    }

    public void setKwDate(String kwDate) {
        this.kwDate = kwDate;
    }
}
