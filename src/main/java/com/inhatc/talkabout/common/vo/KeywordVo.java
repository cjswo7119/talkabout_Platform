package com.inhatc.talkabout.common.vo;

public class KeywordVo {
    private String kwrdNo;
    private String keyword;
    private String kwrdUrl;
    private String title;
    private String kwDate;
    private String kwrdCnt;

    public String getKwrdNo() {
        return kwrdNo;
    }

    public void setKwrdNo(String kwrdNo) {
        this.kwrdNo = kwrdNo;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public String getKwrdUrl() {
        return kwrdUrl;
    }

    public void setKwrdUrl(String kwrdUrl) {
        this.kwrdUrl = kwrdUrl;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getKwDate() {
        return kwDate;
    }

    public void setKwDate(String kwDate) {
        this.kwDate = kwDate;
    }

    public String getKwrdCnt() {
        return kwrdCnt;
    }

    public void setKwrdCnt(String kwrdCnt) {
        this.kwrdCnt = kwrdCnt;
    }
}
